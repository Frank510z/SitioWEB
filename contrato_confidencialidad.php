        obligados a conservar sus datos para cumplir con las leyes aplicables), resolver disputas y hacer cumplir
        nuestros acuerdos y políticas legales.</p>
    <p>Si ya no desea ser nuestro cliente, o dejar de usar o acceder a nuestros Servicios, puede solicitarnos que
        cerremos su cuenta y eliminemos toda la información personal que tenemos sobre usted a través del Centro de
        privacidad, sujeto a ciertos requisitos legales.</p>
    <p>Si nos envía correspondencia, incluidos correos electrónicos, conservamos dicha información electrónicamente en
        los registros de su cuenta. También conservaremos electrónicamente la correspondencia de servicio al cliente y
        otra correspondencia que le enviemos. Conservamos estos registros para medir y mejorar nuestro servicio al
        cliente y para investigar posibles fraudes y violaciones. Es posible que, con el tiempo, eliminemos estos
        registros según lo permita la ley.</p>
    <p>También conservaremos los datos de uso de los servicios para fines de análisis interno. Los datos de uso
        generalmente se retienen por un período de tiempo más corto, excepto cuando estos datos se usan para fortalecer
        la seguridad o mejorar la funcionalidad de nuestro sitio, o cuando estamos legalmente obligados a retener estos
        datos por períodos más largos.</p>

